export class Level {
  readonly cols: number;
  readonly rows: number;
  readonly widthPx: number;
  readonly heightPx: number;
  coins: CoinData[];
  readonly enemySpawns: EnemySpawn[];
  private sprites: GameSprites;

  constructor(sprites: GameSprites) {
    this.sprites = sprites;
    this.cols = MAP[0].length;
    this.rows = MAP.length;
    this.widthPx = this.cols * TILE;
    this.heightPx = this.rows * TILE;
    this.coins = COIN_POSITIONS.map(([cx, cy]) => ({ x: cx * TILE + 4, y: cy * TILE + 4, collected: false }));
    this.enemySpawns = ENEMY_SPAWNS;
  }

  getTile(col: number, row: number): number {
    if (col < 0 || col >= this.cols || row < 0 || row >= this.rows) return 1;
    return MAP[row][col];
  }

  isSolid(col: number, row: number): boolean {
    const t = this.getTile(col, row);
    return t === 1 || t === 2 || t === 4 || t === 5 || t === 6 || t === 7;
  }

  isOneWay(col: number, row: number): boolean {
    return this.getTile(col, row) === 3;
  }

  render(ctx: CanvasRenderingContext2D, camera: Camera): void {
    const startCol = Math.max(0, Math.floor(camera.scrollX / TILE));
    const endCol = Math.min(this.cols, Math.ceil((camera.scrollX + camera.width) / TILE) + 1);
    const startRow = Math.max(0, Math.floor(camera.scrollY / TILE));
    const endRow = Math.min(this.rows, Math.ceil((camera.scrollY + camera.height) / TILE) + 1);

    for (let r = startRow; r < endRow; r++) {
      for (let c = startCol; c < endCol; c++) {
        const tile = MAP[r][c];
        if (tile === 0) continue;
        const px = c * TILE;
        const py = r * TILE;
        let sprite: HTMLCanvasElement | null = null;
        switch (tile) {
          case 1: sprite = this.sprites.floor; break;
          case 2: sprite = this.sprites.shelf; break;
          case 3: sprite = this.sprites.shelfTop; break;
          case 4: sprite = this.sprites.wall; break;
          case 5: sprite = this.sprites.fridge; break;
          case 6: sprite = this.sprites.checkout; break;
          case 7: sprite = this.sprites.ceiling; break;
          case 8: sprite = this.sprites.exitDoor; break;
        }
        if (sprite) ctx.drawImage(sprite, px, py);
      }
    }

    for (const coin of this.coins) {
      if (coin.collected) continue;
      const frame = Math.floor(Date.now() / 150) % 4;
      ctx.drawImage(this.sprites.coin[frame], coin.x - 4, coin.y - 4);
    }
  }
}
